public class GetMaximumConsecutive {

    public int getMaximumConsecutive(int[] coins) {
        Arrays.sort(coins);
        int res = 1;
        for (int a : coins) {
            if (a > res) {
                break;
            }
            res += a;
        }
        return res;
    }
}
