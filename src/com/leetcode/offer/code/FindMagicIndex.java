public class FindMagicIndex {

    public int findMagicIndex2(int[] nums) {
        for (int i = 0; i < nums.length; i++) {
            if (nums[i] == i) {
                return i;
            }
        }
        return -1;
    }

    private int res = -1;

    public int findMagicIndex(int[] nums) {
        bi(nums, 0, nums.length - 1);
        return res;
    }

    public void bi(int[] nums, int l, int r) {
        if (l > r) {
            return;
        }
        int mid = (l + r) / 2;
        if (nums[mid] == mid) {
            res = mid;
        }
        bi(nums, l, mid - 1);
        if (res == -1) {
            bi(nums, mid + 1, r);
        }
    }
}
